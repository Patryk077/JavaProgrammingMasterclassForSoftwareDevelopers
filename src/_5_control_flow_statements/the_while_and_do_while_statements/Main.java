package _5_control_flow_statements.the_while_and_do_while_statements;

public class Main {

    public static void main(String[] args) {
        int count = 1;
        while (count != 6) {
            System.out.println("Count value is " + count);
            count++;
        }
        for (int i = 1; i < 7; i++) {
            System.out.println("Count value is " + i);
        }
    }
}
